Page({
  data: {
    type: 'user',
    title: '',
    content: ''
  },
  onLoad(options) {
    this.applyType(options.type === 'privacy' ? 'privacy' : 'user')
  },
  applyType(type) {
    const isPrivacy = type === 'privacy'
    this.setData({
      type,
      title: isPrivacy ? '隐私政策' : '用户服务协议',
      content: isPrivacy ? PRIVACY_POLICY : USER_AGREEMENT
    })
    wx.setNavigationBarTitle({ title: isPrivacy ? '隐私政策' : '用户服务协议' })
  },
  switchType(e) {
    this.applyType(e.currentTarget.dataset.type)
  }
})
